public class decrement {
    public static void main(String[] args) {
        int a = 17;
        int b = 2;
        System.out.println(a--);
        System.out.println(--b);
        System.out.println(a++);
        System.out.println(--b);
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a);
    }
}